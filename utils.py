"""
Utility functions for LeetCode Exporter.

Provides helper functions for folder naming, file extension detection,
README generation, CSV export, and statistics computation.
"""

import csv
import re
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from config import LANGUAGE_EXTENSIONS, LANGUAGE_NAMES


def slugify(title: str) -> str:
    """
    Convert a problem title to a URL-safe slug.

    Args:
        title: The problem title (e.g., "Two Sum").

    Returns:
        A hyphenated slug (e.g., "Two-Sum").
    """
    # Remove non-alphanumeric characters except spaces and hyphens
    cleaned = re.sub(r"[^\w\s-]", "", title)
    # Replace whitespace with hyphens
    return re.sub(r"[\s]+", "-", cleaned).strip("-")


def make_folder_name(question_id: str, title: str) -> str:
    """
    Create a standardized folder name for a problem.

    Args:
        question_id: The problem's frontend ID (e.g., "1").
        title: The problem title (e.g., "Two Sum").

    Returns:
        A formatted folder name (e.g., "0001-Two-Sum").
    """
    padded_id = str(question_id).zfill(4)
    slug = slugify(title)
    return f"{padded_id}-{slug}"


def get_file_extension(language: str) -> str:
    """
    Get the file extension for a programming language.

    Args:
        language: The LeetCode language slug (e.g., "python3").

    Returns:
        The file extension including dot (e.g., ".py").
    """
    return LANGUAGE_EXTENSIONS.get(language.lower(), ".txt")


def get_language_name(language: str) -> str:
    """
    Get the human-readable name for a programming language.

    Args:
        language: The LeetCode language slug (e.g., "python3").

    Returns:
        The display name (e.g., "Python 3").
    """
    return LANGUAGE_NAMES.get(language.lower(), language.capitalize())


def format_timestamp(timestamp: int | float | None) -> str:
    """
    Convert a UNIX timestamp to a human-readable date string.

    Args:
        timestamp: UNIX timestamp in seconds, or None.

    Returns:
        Formatted date string (e.g., "2024-01-15"), or "N/A".
    """
    if timestamp is None:
        return "N/A"
    try:
        dt = datetime.fromtimestamp(int(timestamp), tz=timezone.utc)
        return dt.strftime("%Y-%m-%d")
    except (ValueError, OSError):
        return "N/A"


