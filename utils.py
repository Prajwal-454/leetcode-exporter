def _get_sort_key(p: dict[str, Any]) -> tuple[int, str]:
    """Helper to safely extract a numeric sort key from a question ID."""
    qid = str(p.get("question_id", "0"))
    try:
        return (int(qid), "")
    except ValueError:
        digits = "".join(c for c in qid if c.isdigit())
        return (int(digits) if digits else 0, qid)


def generate_repo_readme(
    problems: list[dict[str, Any]],
    last_updated: str | None = None,
) -> str:
    """
    Generate the repository-level README.md.

    Args:
        problems: List of problem dicts with keys: question_id, title,
                  difficulty, title_slug, language, timestamp.
        last_updated: Optional override for the last-updated timestamp.

    Returns:
        Formatted repository README markdown string.
    """
    total = len(problems)
    easy = sum(1 for p in problems if p.get("difficulty") == "Easy")
    medium = sum(1 for p in problems if p.get("difficulty") == "Medium")
    hard = sum(1 for p in problems if p.get("difficulty") == "Hard")

    # Collect unique languages
    languages = sorted(set(get_language_name(p.get("language", "")) for p in problems))
    languages_str = ", ".join(languages) if languages else "N/A"

    updated = last_updated or datetime.now(tz=timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    # Sort problems by ID
    sorted_problems = sorted(problems, key=_get_sort_key)

    # Build problem table rows
    table_rows = []
    for p in sorted_problems:
        qid = str(p["question_id"]).zfill(4)
        title = p["title"]
        slug = p["title_slug"]
        diff = p["difficulty"]
        lang = get_language_name(p.get("language", ""))
        folder = make_folder_name(p["question_id"], title)
        url = f"https://leetcode.com/problems/{slug}/"
        date = format_timestamp(p.get("timestamp"))

        table_rows.append(
            f"| {qid} | [{title}]({url}) | [{folder}](./{folder}/) | {diff} | {lang} | {date} |"
        )

    table = "\n".join(table_rows)

    return f"""# 🏆 LeetCode Solutions

<div align="center">

![Total](https://img.shields.io/badge/Total_Solved-{total}-blue?style=for-the-badge)
![Easy](https://img.shields.io/badge/Easy-{easy}-00b8a3?style=for-the-badge)
![Medium](https://img.shields.io/badge/Medium-{medium}-ffc01e?style=for-the-badge)
![Hard](https://img.shields.io/badge/Hard-{hard}-ff375f?style=for-the-badge)

</div>

## 📊 Statistics

| Metric | Count |
|--------|-------|
| **Total Solved** | {total} |
| **Easy** | {easy} |
| **Medium** | {medium} |
| **Hard** | {hard} |
| **Languages** | {languages_str} |
| **Last Updated** | {updated} |

## 📁 Solutions

| # | Problem | Solution | Difficulty | Language | Date |
|---|---------|----------|------------|----------|------|
{table}

---

## 🚀 Setup

This repository is auto-generated by **LeetCode Exporter**.

See the [exporter README](https://github.com/) for setup instructions.

---
*Auto-generated on {updated}*
"""


def export_to_csv(problems: list[dict[str, Any]], output_path: Path) -> Path:
    """
    Export solved problems to a CSV file.

    Args:
        problems: List of problem dictionaries.
        output_path: Directory to save the CSV file.

    Returns:
        Path to the created CSV file.
    """
    csv_path = output_path / "solutions.csv"
    sorted_problems = sorted(problems, key=_get_sort_key)

    fieldnames = [
        "ID", "Title", "Difficulty", "Language", "Tags", "URL", "Date Solved"
    ]

    with open(csv_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for p in sorted_problems:
            writer.writerow({
                "ID": str(p["question_id"]).zfill(4),
                "Title": p["title"],
                "Difficulty": p["difficulty"],
                "Language": get_language_name(p.get("language", "")),
                "Tags": "; ".join(p.get("tags", [])),
                "URL": f"https://leetcode.com/problems/{p['title_slug']}/",
                "Date Solved": format_timestamp(p.get("timestamp")),
            })

    return csv_path


def compute_statistics(problems: list[dict[str, Any]]) -> dict[str, Any]:
    """
    Compute summary statistics for solved problems.

    Args:
        problems: List of problem dictionaries.

    Returns:
        Dictionary containing difficulty and language breakdowns.
    """
    stats: dict[str, Any] = {
        "total": len(problems),
        "by_difficulty": {"Easy": 0, "Medium": 0, "Hard": 0},
        "by_language": {},
    }

    for p in problems:
        diff = p.get("difficulty", "Unknown")
        lang = get_language_name(p.get("language", ""))

        if diff in stats["by_difficulty"]:
            stats["by_difficulty"][diff] += 1

        stats["by_language"][lang] = stats["by_language"].get(lang, 0) + 1

    return stats
